package com.hsl.library.entity;

public class District {
    public int left;
    public int top;
    public int right;
    public int bottom;
    public int color;

    public District(int left, int top, int right, int bottom,int color) {
        this.left = left;
        this.top = top;
        this.right = right;
        this.bottom = bottom;
        this.color = color;
    }
}
